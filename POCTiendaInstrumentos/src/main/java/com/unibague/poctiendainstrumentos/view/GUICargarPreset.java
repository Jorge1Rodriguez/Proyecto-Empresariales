    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        if (txtCodigo.getText().isBlank() || txtNombre.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Complete los campos", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            Teclado teclado = (Teclado) servicioInstrumento.buscarInstrumento(txtCodigo.getText());
            JOptionPane.showMessageDialog(this, teclado.cargarPreset(txtNombre.getText()));
            txtCodigo.setText("");
            txtNombre.setText("");
        }
    }//GEN-LAST:event_btnCargarActionPerformed


